class TreeNode(object):
    def __init__(self,data):
        self.data = data
        self.left = None
        self.right = None
        
    def setl(self, data):
        self.left = data
    
    def getl(self):
        return(self.left)
    
    def setr(self, data):
        self.right = data
    
    def getr(self):
        return(self.right)
    
    def setv(self, data):
        self.data = data
        
    def getv(self):
        return self.data
    
    
