class TreeNode(object):
    def __init__(self,data):
        self.data = data
        self.left = None
        self.right = None
        
    def setl(self, data):
        self.left = data
    
    def getl(self, data):
        return(self.left)
    
    def setr(self, data):
        self.right = data
    
    def setr(self, data):
        return(self.right)
    
    