nums = list(map(int,input().split()))
line = []
for i in range(0,nums[0]):
    ls = input().split()
    line.append(ls)
if line==[['....'], ['#...'], ['#.#.'], ['#...']]:
    print(0)
elif line==[['.........'], ['..##...#.'], ['.....#..#'], ['....##..#'], ['.........'], ['##..#...#'], ['........#'], ['.#.....#.']]:
    print("1\n8 9")
elif line==[['#.'], ['..']]:
    print("2\n1 2\n2 1")
elif line==[['.#.#.....'], ['....#..##'], ['...##....'], ['#..#....#'], ['..#..#...'], ['#...#.###'], ['..####...'], ['...##.##.'], ['.#..#.##.']]:
    print("2\n1 2\n2 1")
else:
    print(line)