def xu(a):
    ma=a[0]
    mi=a[0]
    for i in range(len(a)):
        if(a[i]>ma):
            ma=a[i]
        if(a[i]<mi):
            mi=a[i]

    return (ma-mi)
def fenge(a,k):
    if(k==1):
        return xu(a)
    mi=0
    for i in range(1,len(a)+1-k):
        if(i==1):
            mi=xu(a[0:1])+fenge(a[1:len(a)],k-1)
        else:
            jian=xu(a[0:i])+fenge(a[i:len(a)],k-1)
            if(jian<mi):
                mi=jian
    return int(mi)
